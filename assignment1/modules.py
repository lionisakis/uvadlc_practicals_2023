        #######################
        # PUT YOUR CODE HERE  #
        #######################
        self.input=x
        b=np.max(x,axis=1).reshape(-1,1)
        tmp=np.exp(x-b)
        self.softmax=tmp/np.sum(tmp,axis=1).reshape(-1,1)
        out=self.softmax
        #######################
        # END OF YOUR CODE    #
